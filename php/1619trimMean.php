class Solution {

    /**
     * @param Integer[] $arr
     * @return Float
     */
    function trimMean($arr) {
        sort($arr);
        $count = count($arr);
        $cut = $count * 0.05;
        $total = 0;
        for ($i = $cut; $i < $count - $cut; $i++) {
            $total = bcadd($arr[$i], $total);
        }
        var_dump($total, round($total / ($count - 2 * $cut), 5));die;
        return round($total / ($count - 2 * $cut), 5);
    }
}

